package classes;

public class Carta {
	String nomeCarta, imagem, tipoDaCarta, cartaDescricao;
	int mana, ataque, defesa;
	
	public String getNomeCarta() {
		return nomeCarta;
	}
	public void setNomeCarta(String nomeCarta) {
		this.nomeCarta = nomeCarta;
	}
	
	public String getImagem() {
		return imagem;
	}
	public void setImagem(String imagem) {
		this.imagem = imagem;
	}
	
	public String getTipoDaCarta() {
		return tipoDaCarta;
	}
	public void setTipoDaCarta(String tipoDaCarta) {
		this.tipoDaCarta = tipoDaCarta;
	}
	
	public String getCartaDescricao() {
		return cartaDescricao;
	}
	public void setCartaDescricao(String cartaDescricao) {
		this.cartaDescricao = cartaDescricao;
	}
	
	public int getMana() {
		return mana;
	}
	public void setMana(int mana) {
		this.mana = mana;
	}
	
	public int getAtaque() {
		return ataque;
	}
	public void setAtaque(int ataque) {
		this.ataque = ataque;
	}
	
	public int getDefesa() {
		return defesa;
	}
	public void setDefesa(int defesa) {
		this.defesa = defesa;
	}
	
	
}
